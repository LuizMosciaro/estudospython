#Faça um Programa que verifique se uma letra digitada é vogal ou consoante. 

a = input('Digite uma letra: \n')

if a == 'a' or 'A':
    print(a,' é uma vogal')

elif a == 'e' or 'E':
    print(a,' é uma vogal')

elif a == 'i' or 'I':
    print(a,' é uma vogal')

elif a == 'o' or 'O':
    print(a,' é uma vogal')

elif a == 'u' or 'U':
    print(a,' é uma vogal')

else:
    print(a,' é consoante')